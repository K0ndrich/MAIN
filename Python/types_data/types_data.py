# НЕ Изменяемые Типы Данных
# целое число
my_int = 1
# числа с плавающей точкой
my_float = 3.2
# строка
my_str = "Text"
# логическое значение
my_bool = True
# кортеж (значение нельзя изменять)
my_tuple = (1, 2.3, True, "Text")


