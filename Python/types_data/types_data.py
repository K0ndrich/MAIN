# Изменяемые Типы Данных
# список = [ значение , значение]
my_list = [1, 2.3, True, "Text"]
# словарь = { ключ : значение }
my_dict = {1: "Yes", 2: "No"}
# множетсво = {значение , значение}
my_set = {1, 2.3, True, "Text"}


# пользовательский класс
class MyClass:
    a = 1
    b = 2

    def my_method():
        pass


